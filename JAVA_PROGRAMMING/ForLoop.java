import java.io.*;
import java.util.*;
import java.util.Scanner;


 class ForLoopExample {
    public static void main(String[] args) {
        // Using a for loop to print numbers from 1 to 10
        System.out.println("Using For Loop:");
        for (int i = 1; i <= 10; i++) {
            System.out.println(i);
        }
    }
}